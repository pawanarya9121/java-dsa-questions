public class DataType {
   public static void main(String[] args) {
      int num = 1 ;
      char ch = 'c' ;
      String str = "string" ;
      float ft = 2.0f ;
      double db = 22.22 ;
      boolean bl = true ;

      System.out.println(num);
      System.out.println(ch);
      System.out.println(str);
      System.out.println(ft);
      System.out.println(db);
      System.out.println(bl);
   }
}
